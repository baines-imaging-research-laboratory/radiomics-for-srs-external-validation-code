    methods (Access = public, Static)
        
        function mXxFnValues = ApplyScalarFunctionToEachIndex(mXxData, fnFunction)
            cXxFnValues = cell(size(mXxData));
            
            for dIndex=1:numel(mXxData)
                cXxFnValues{dIndex} = fnFunction(mXxData(dIndex));
            end
            
            mXxFnValues = CellArrayUtils.CellArrayOfObjects2MatrixOfObjects(cXxFnValues);
        end
    end
    
    
    
    % *********************************************************************   ORDERING: 1 Abstract     -> X.1 Not Static
    % *                         PRIVATE METHODS                           *             2 Not Abstract    X.2 Static
